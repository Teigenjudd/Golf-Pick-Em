// Regenerates .design-sync/preview-data.js from the app's demo fixture.
// Run from the repo root:  node .design-sync/gen-preview-data.mjs
// Committed (not under the gitignored .ds-sync/) so re-syncs are reproducible.
import { demoTournament, demoTiers, demoParticipants, demoLeaderboardData } from '../src/demo/demoData.js';
import { computeScores, assignRanks } from '../src/utils/scoring.js';
import { writeFileSync } from 'node:fs';

// Standings computed from the FULL fixture (Node reads UTF-8 fine).
const standings = assignRanks(computeScores({
  picks: demoParticipants,
  leaderboardData: demoLeaderboardData,
  scoresToKeep: demoTournament.scores_to_keep,
}));

// Keep leaderboard rows that any pick references (so TierValueWidget resolves)
// plus the top rows (for PGALeadersWidget) — compact but correct.
const pickedIds = new Set(demoParticipants.map(p => String(p.player_id)));
const rows = demoLeaderboardData.leaderboardRows;
const kept = rows.filter((r, i) => i < 12 || pickedIds.has(String(r.playerId)));
const leaderboardData = { ...demoLeaderboardData, leaderboardRows: kept };

// Tiers trimmed to 6 players each for the picker card.
const tiers = demoTiers.map(t => ({ ...t, tier_players: t.tier_players.slice(0, 6) }));

const currentUserId = demoParticipants[0].user_id; // highlights one real row as "you"

const J = (v) => JSON.stringify(v, null, 2);
const body = `// AUTO-GENERATED by .design-sync/gen-preview-data.mjs from src/demo/demoData.js.
// Static, pre-computed props for the design-sync preview cards — no scoring
// import (so the toxic-regex module never enters the preview bundle) and no
// live computation. Regenerate if the demo fixture changes.
export const tournament = ${J(demoTournament)};
export const tiers = ${J(tiers)};
export const picks = ${J(demoParticipants)};
export const leaderboardData = ${J(leaderboardData)};
export const standings = ${J(standings)};
export const currentUserId = ${J(currentUserId)};
`;
writeFileSync(new URL('./preview-data.js', import.meta.url), body);
console.log('wrote preview-data.js:', standings.length, 'standings,', kept.length, 'leaderboard rows,', tiers.length, 'tiers');
console.log('sample standing keys:', Object.keys(standings[0]).join(', '));
